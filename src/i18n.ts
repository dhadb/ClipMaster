export type TranslationKey = keyof typeof zh

const translations: Record<ResolvedLanguage, Record<TranslationKey, string>> = {
  'zh-CN': zh,
  'en-US': en,
}

export function resolveLanguage(setting: LanguageSetting = 'system', locale?: string): ResolvedLanguage {
  if (setting === 'zh-CN' || setting === 'en-US') return setting
  const detected = (locale || (typeof navigator !== 'undefined' ? navigator.language : '')).toLowerCase()
  return detected.startsWith('zh') ? 'zh-CN' : 'en-US'
}

export function formatMessage(template: string, params: Params = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export function translate(language: ResolvedLanguage, key: TranslationKey, params?: Params) {
  return formatMessage(translations[language][key] ?? translations['zh-CN'][key] ?? key, params)
}

export function useI18n() {
  const setting = useClipboardStore(s => s.settings.language)
  const language = useMemo(() => resolveLanguage(setting), [setting])
  return useMemo(() => ({
    language,
    t: (key: TranslationKey, params?: Params) => translate(language, key, params),
    typeLabel: (type: string) => translate(language, `type.${type}` as TranslationKey),
  }), [language])
}
